const FRAC_BITS = 16;
const SCALE = 1 << FRAC_BITS; // 也就是 2^16

// 輔助函式：浮點數轉定點數
function toFixed(float_val) {
    return Math.floor(float_val * SCALE);
}

// 輔助函式：定點數轉回整數 (用來查地圖陣列)
function toInt(fixed_val) {
    // return fixed_val >> FRAC_BITS;
    return Math.floor(fixed_val / SCALE);
}

function getFloat(fixed_val) {
    return fixed_val / SCALE;
}

function getFloatPart(fixed_val) {
    return fixed_val & (SCALE - 1);
}

function multQ16(a, b){
    return Math.floor(a*b/SCALE);
}





