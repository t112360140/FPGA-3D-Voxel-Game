// https://en.wikipedia.org/wiki/Linear-feedback_shift_register#Galois_LFSRs
let lfsr = new Uint16Array([12345]);
function initRand(seed){
    lfsr = new Uint16Array([seed+32521]);
    if(lfsr[0]==0) lfsr[0]=10384;
}
function rand(){
    const lsb = lfsr[0] & 0x001;       /* Get LSB (i.e., the output bit). */
    lfsr[0] >>= 1;                     /* Shift register */
    if (lsb)                           /* If the output bit is 1, */
        lfsr[0] ^= 0xB400;             /*  apply toggle mask. */
    return lfsr[0]
}


//----線性回饋移位暫存器(32bit)----
let lfsr32 = new Uint32Array([0xAC0140EC]); // 初始種子
function rand32_init(seed){
    if(seed==0) lfsr32[0]=0xAC0140EC;
    else lfsr32[0] = seed>>>0;
}
function rand32() {
    let lsb = lfsr32[0] & 1;
    lfsr32[0] >>>= 1; // 使用無符號右移
    if (lsb) {
        // 32-bit 的常用多項式遮罩 (Taps: 32, 31, 29, 1)
        lfsr32[0] ^= 0x80000057; 
    }
    return lfsr32[0];
}

