// 將 0~1023 轉換為弧度 (模擬查表)
function angleToRadian(angle_1024) {
    return (angle_1024 / 1024.0) * 2.0 * Math.PI;
}

const COS_TABLE=new Array(257).fill(0).map((_, i)=>(toFixed(Math.cos(angleToRadian(i)))));
function cos(x) {
    x = abs(x) % 1024;
    if (x < 256) return COS_TABLE[x];           // 第一象限
    if (x < 512) return -COS_TABLE[512 - x];    // 第二象限
    if (x < 768) return -COS_TABLE[x - 512];    // 第三象限
    return COS_TABLE[1024 - x];                 // 第四象限
}
function sin(x){
    return cos(x-256);
}


// ---- 1/dir 查表 (Hardware ROM 模擬) ----
const INVDELTA_RESOLUTION = 1024; // 1.0 對應的索引值 (10-bit 小數)
const INVDELTA_TABLE_SIZE = 4096; // 支援 dir 的絕對值到接近 4.0
const INVDELTA_ROM = new Uint32Array(INVDELTA_TABLE_SIZE);

// 預先計算好所有可能的 1/dir 並轉成 Q16.16
for (let i = 0; i < INVDELTA_TABLE_SIZE; i++) {
    let val = i / INVDELTA_RESOLUTION; 
    if (val === 0) {
        // 當 dir 為 0 時，1/dir 應該是無限大。
        // 我們給予一個極大的 31-bit 整數代表無窮遠，這樣射線就不會在該軸上跨步
        INVDELTA_ROM[i] = 0x7FFFFFFF; 
    } else {
        // 先計算浮點數除法，然後立刻轉換為 Q16.16 定點數
        INVDELTA_ROM[i] = toFixed(abs(1.0 / val));
    }
}

function one_div(x){
    if (x >= INVDELTA_TABLE_SIZE) x = INVDELTA_TABLE_SIZE - 1;
    return INVDELTA_ROM[x];
}

function Q16_to_Q10(value){
    return abs(value)>>>(FRAC_BITS-10);
}



