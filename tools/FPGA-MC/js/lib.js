function fade(t) {
    return t * t * t * (t * (t * 6 - 15) + 10);
}

function lerp(a, b, t) {
    return a + t * (b - a);
}

function grad(hash, x, y) {
    // 將 hash 值的最後兩位拿來決定方向 (4個方向)
    const h = hash & 3;
    const u = h < 2 ? x : y;
    const v = h < 2 ? y : x;
    return ((h & 1) === 0 ? u : -u) + ((h & 2) === 0 ? v : -v);
}

function perlin(x, y, seed=1234, scale=0.1) {
    seed*=123.4;
    x=(x+(seed^0x3024)-323.4)*scale;
    y=(y+(seed^0x3024)+234.5)*scale;

    // 找出所在晶格的整數座標 (0-255)
    let X = Math.floor(x) & 255;
    let Y = Math.floor(y) & 255;

    // 找出在晶格內的相對位置 (0.0 - 1.0)
    let xf = x - Math.floor(x);
    let yf = y - Math.floor(y);

    // 計算緩和曲線權重
    let u = fade(xf);
    let v = fade(yf);

    // 取得四個角點的雜湊索引
    let aa = p[p[X] + Y];
    let ab = p[p[X] + Y + 1];
    let ba = p[p[X + 1] + Y];
    let bb = p[p[X + 1] + Y + 1];

    // 對四個角點進行梯度點積與線性插值
    let x1 = lerp(grad(aa, xf, yf), grad(ba, xf - 1, yf), u);
    let x2 = lerp(grad(ab, xf, yf - 1), grad(bb, xf - 1, yf - 1), u);

    // 最終結果映射到 [0, 1] 區間
    return (lerp(x1, x2, v) + 1) / 2;
}





