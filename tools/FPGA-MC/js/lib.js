class VGA{
    constructor(canvas, height=240, width=320, scale=2){
        this.canvas=typeof canvas=='string'?document.getElementById(canvas):canvas;
        this.ctx=this.canvas.getContext("2d");
        this.height=height;
        this.width=width;
        this.scale=scale;

        this.canvas.height=this.height*this.scale;
        this.canvas.width=this.width*this.scale;

        this.pixels=this.height*this.width;
        this.buffer=new Uint8Array(this.pixels);
    }

    draw(){
        for(let y=0;y<this.height;y++){
            for(let x=0;x<this.width;x++){
                const pixel=this.buffer[y*this.width+x];
                this.ctx.fillStyle=`rgb(${((pixel>>5)&(0x07))*36}, ${((pixel>>2)&(0x07))*36}, ${((pixel)&(0x03))*85})`;
                this.ctx.fillRect(x*this.scale, y*this.scale, this.scale, this.scale);
            }
        }
    }

    /**
     * 
     * @param {Number} pixel 
     * @param {Number} r 0~7
     * @param {Number} g 0~7
     * @param {Number} b 0~3
     */
    setPixelRGB(pixel, r=0, g=0, b=0){
        this.buffer[pixel]=(r<<5)|(g<<2)|(b);
    }
    setPixelColor(pixel, color=0){
        this.buffer[pixel]=color;
    }
}




